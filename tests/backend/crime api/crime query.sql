SELECT
    Crime_Category,
    SUM(`2021_10`+`2021_11`+`2021_12`+`2022_01`+`2022_02`+`2022_03`+`2022_04`+`2022_05`+`2022_06`+`2022_07`+`2022_08`+`2022_09`+`2022_10`+`2022_11`+`2022_12`+`2023_01`+`2023_02`+`2023_03`+`2023_04`+`2023_05`+`2023_06`+`2023_07`+`2023_08`+`2023_09`) AS TotalCrimeCount
FROM
    crime_data
WHERE
    LSOA_Code = 'E01000006'
GROUP BY
    Crime_Category;